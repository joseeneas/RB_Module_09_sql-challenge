-- ********************************************************************
-- 1. LIST THE EMPLOYEE NUMBER,
--             LAST NAME,
--             FIRST NAME,
--             SEX,
--         AND SALARY
--     OF EACH EMPLOYEE.
--
-- SINTAX OPTION 1
--
SELECT E.EMP_NO,
	S.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME,
	E.SEX,
	S.SALARY
FROM EMPLOYEES E,
	SALARIES S
WHERE E.EMP_NO = S.EMP_NO
ORDER BY E.EMP_NO;
--
-- SINTAX OPTION 2
--
SELECT E.EMP_NO,
	S.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME,
	E.SEX,
	S.SALARY
FROM EMPLOYEES E
INNER JOIN SALARIES S ON E.EMP_NO = S.EMP_NO
ORDER BY E.EMP_NO;
-- ********************************************************************
-- 2. LIST THE FIRST NAME,
--             LAST NAME,
--         AND HIRE DATE FOR THE EMPLOYEES WHO WERE HIRED IN 1986.
--
-- SINTAX OPTION 1
--
SELECT E.FIRST_NAME,
	E.LAST_NAME,
	E.HIRE_DATE
FROM EMPLOYEES E
WHERE DATE_PART('YEAR',	HIRE_DATE) = 1986;
-- ********************************************************************
-- 3. LIST THE MANAGER OF EACH DEPARTMENT ALONG WITH THEIR
--      DEPARTMENT NUMBER,
--      DEPARTMENT NAME,
--      EMPLOYEE NUMBER,
--      LAST NAME,
--      AND FIRST NAME.
--
-- SINTAX OPTION 1
--
SELECT M.DEPT_NO,
	D.DEPT_NAME,
	M.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME
FROM DEPT_MANAGER M,
	DEPARTMENT D,
	EMPLOYEES E
WHERE M.DEPT_NO = D.DEPT_NO
	AND M.EMP_NO = E.EMP_NO;
--
-- SINTAX OPTION 2
--
SELECT M.DEPT_NO,
	D.DEPT_NAME,
	M.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME
FROM DEPT_MANAGER M
INNER JOIN DEPARTMENT D ON M.DEPT_NO = D.DEPT_NO
INNER JOIN EMPLOYEES E ON M.EMP_NO = E.EMP_NO;
