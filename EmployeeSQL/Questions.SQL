-- ********************************************************************
-- 5. LIST FIRST NAME,
--         LAST NAME,
--	   AND SEX OF EACH EMPLOYEE
--     WHOSE FIRST NAME IS HERCULES AND
--     WHOSE LAST NAME BEGINS WITH THE LETTER B.
--
-- SINTAX OPTION 1
--
SELECT E.FIRST_NAME,
	E.LAST_NAME,
	E.SEX
FROM EMPLOYEES E
WHERE E.FIRST_NAME = 'HERCULES'
	AND E.LAST_NAME LIKE 'B%';
-- ********************************************************************
-- 6. LIST EACH EMPLOYEE IN THE SALES DEPARTMENT,
--    INCLUDING THEIR EMPLOYEE NUMBER,
--    LAST NAME,
--AND FIRST NAME.
--
-- SINTAX OPTION 1
--
SELECT M.DEPT_NO,
	D.DEPT_NAME,
	M.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME
FROM DEPT_EMP M,
	DEPARTMENT D,
	EMPLOYEES E
WHERE M.DEPT_NO = D.DEPT_NO
	AND M.EMP_NO = E.EMP_NO
	AND D.DEPT_NAME = 'SALES';
--
-- SINTAX OPTION 2
--
SELECT M.DEPT_NO,
	D.DEPT_NAME,
	M.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME
FROM DEPT_EMP M
INNER JOIN DEPARTMENT D ON M.DEPT_NO = D.DEPT_NO
INNER JOIN EMPLOYEES E ON M.EMP_NO = E.EMP_NO
WHERE D.DEPT_NAME = 'SALES';
-- ********************************************************************
-- 7. LIST EACH EMPLOYEE
--    IN THE SALES AND DEVELOPMENT DEPARTMENTS,
--    INCLUDING THEIR EMPLOYEE NUMBER, LAST NAME, FIRST NAME, AND DEPARTMENT NAME.
--
-- SINTAX OPTION 1
--
SELECT M.DEPT_NO,
	D.DEPT_NAME,
	M.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME
FROM DEPT_EMP AS M,
	DEPARTMENT AS D,
	EMPLOYEES E
WHERE M.DEPT_NO = D.DEPT_NO
	AND M.EMP_NO = E.EMP_NO
	AND (D.DEPT_NAME = 'SALES'
						OR D.DEPT_NAME = 'DEVELOPMENT');
--
-- SINTAX OPTION 2
--
SELECT M.DEPT_NO,
	D.DEPT_NAME,
	M.EMP_NO,
	E.LAST_NAME,
	E.FIRST_NAME
FROM DEPT_EMP M
INNER JOIN DEPARTMENT D ON M.DEPT_NO = D.DEPT_NO
INNER JOIN EMPLOYEES E ON M.EMP_NO = E.EMP_NO
WHERE (D.DEPT_NAME = 'SALES' OR D.DEPT_NAME = 'DEVELOPMENT');
-- ********************************************************************
-- 8.  LIST THE FREQUENCY COUNTS,
--               IN DESCENDING ORDER,
--				OF ALL THE EMPLOYEE LAST NAMES (THAT IS, HOW MANY EMPLOYEES SHARE EACH LAST NAME).
--
-- SINTAX OPTION 1
--
SELECT LAST_NAME,
	COUNT(LAST_NAME)
FROM EMPLOYEES
GROUP BY LAST_NAME
ORDER BY COUNT(LAST_NAME) DESC;